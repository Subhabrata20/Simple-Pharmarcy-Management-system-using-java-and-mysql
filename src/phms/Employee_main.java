     private void showDate() {
        d = new Date();
         dd = new SimpleDateFormat("dd-MM-yyyy");
        today.setText(dd.format(d));
        
        new Timer(0, (ActionEvent ae) -> {
            Date d1 = new Date();
            SimpleDateFormat dd1 = new SimpleDateFormat("hh:mm:ss a");
            time.setText(dd1.format(d1));
        }).start();
    }
